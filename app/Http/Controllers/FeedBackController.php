     * Store a newly created resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->only('user_name', 'user_email', 'user_message');
        $filename = $data['user_name'] . ' ' . now();
        $json = json_encode($data);
        file_put_contents(public_path('feedBack/' . $filename . '.json'), $json);

        return redirect()->route('welcome.index')->with('feedBackSent', 'Message sent. We read all our feed backs and will answer you shortly.');
    }

